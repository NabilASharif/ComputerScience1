public class SunFacts
{
	public static void main(String[] args)
	{
		double distance, mass;
		
		distance = 1.45979E11;
		mass = 1.989E30;
		
		System.out.println("The sun is " + distance +
				" meters away. ");
		System.out.println("The sun's mass is " + mass +
				" kilograms");
	}
}

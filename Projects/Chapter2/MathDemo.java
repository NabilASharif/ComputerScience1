public class MathDemo
{
	public static void main(String []args)
	{
		double monday, tuesday, wednesday, average;
		
		monday = 100.0;
		tuesday = 200.0;
		wednesday = 300.0;
		average = (monday + tuesday + wednesday) / 3;
		System.out.println("The average is " + average);
		
	}
}